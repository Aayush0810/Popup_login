function renderCountries() {
  const countryListElement = document.getElementById("allCountryListDropdown");
  console.log("EXECUTION STARTS");
  for (let key in countryList) {
    let html = `
    <li>
    <a
      href="#"
      onclick="selectCountry(this)"
      id="${key}"
      class="block py-2 px-4 hover:bg-gray-100 dark:hover:bg-gray-600 dark:hover:text-white text-black"
      >${countryList[key]}</a
    >
  </li>
    `;

    countryListElement.innerHTML += html;
  }
}

